public class i_array {
    public static void main(String[] args) {
        String[] array1 = {"Thanks", "Gracias", "Merci"};
        int[] array2 = {1,2,3,4};
        int[][] array3 = {{1,2,3}, {4,5,6}};

        
        System.out.println(array1[1]);
        System.out.println(array2[2]);
        System.out.println(array3[0][1]);
    }
}
